from pwn import *

target = bytearray([
0x2d, 0x29, 0x31, 0x34, 0x2c, 0x32, 0x27, 0x30,
0x2b, 0x24, 0x4c, 0x39, 0x6f, 0x44, 0x73, 0x37,
0x32, 0x74, 0x36, 0x3b, 0x77, 0x57, 0x4d, 0x8c,
0x71, 0x50, 0x79, 0x3f, 0x3d, 0x50, 0x69, 0x38,
])

for i, c in enumerate(target):
    target[i] = (c - i) % 256


for i, c in enumerate(target):
    target[i] = (c + 0x69) % 256


print(xor(target, b'\xff'))

